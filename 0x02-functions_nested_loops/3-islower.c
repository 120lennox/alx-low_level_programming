#include "main.h"
/**
 *_islower - checks if character is lower case
 *@c: stores character to be checked
 *Return: 0 on success
 */
int _islower(int c)
{
c = _islower('H');
_putchar(c + '0');
c = _islower('0');
_putchar(c + '0');
c = _islower(108);
_putchar(c + '0');
c = _islower(';');
_putchar(c + '0');
_putchar('\n');
return (0);
}
